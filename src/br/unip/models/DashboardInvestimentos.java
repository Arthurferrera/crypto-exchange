package br.unip.models;

public class DashboardInvestimentos {
	
	private String total;
	private String investido;
	private String caixa;
	private String aportesMes;
	
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getInvestido() {
		return investido;
	}
	public void setInvestido(String investido) {
		this.investido = investido;
	}
	public String getCaixa() {
		return caixa;
	}
	public void setCaixa(String caixa) {
		this.caixa = caixa;
	}
	public String getAportesMes() {
		return aportesMes;
	}
	public void setAportesMes(String aportesMes) {
		this.aportesMes = aportesMes;
	}
	
	
}
